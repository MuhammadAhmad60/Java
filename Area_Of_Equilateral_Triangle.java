
//Area Of Equilateral Triangle


public class Area_Of_Equilateral_Triangle {
    public static void main(String[] args) {
        // formula  area = square(3)/4 * (a * a)

        double Area;
        double sides = 2;

        Area = (Math.sqrt(3)/4) * (sides * sides);
        System.out.println("The Area Of Equilateral Triangle  = " + Area);
    }
}
